package com.nutresa.exemplary_provider.dtl;

public class AccessDTO {
    private String id;
    private String api;
    private String action;

    public String getApi() {
        return api;
    }

    public void setApi(String api) {
        this.api = api;
    }

    public String getAction() {
        return action;
    }

    public void setAction(String action) {
        this.action = action;
    }

    public String getId() {
        return id;
    }

}
